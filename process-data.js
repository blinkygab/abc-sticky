const data = getData(); // see below for stub
const output = [];

function Season(p) {
  return {
    season: p.seasonStartDate,
    programs: []
  }
}

function Program(p) {
  return {
    program: p.name,
    DayOfWeek: p.DayOfWeek,
    alternateMop: p.alternateMop,
    alternateName: p.alternateName,
    episodes: []
  }
}

function Episode(p) {
  return { 
    scheduledDateId: p.scheduledDateId,
    airTime: p.airTime,
    startTime: p.startTime,
    endTime: p.endTime
  }
}

data.forEach(p=>{ 
  let season = output.find(x => x.season === p.seasonStartDate);
  if (!season) {
    season = Season(p);
    output.push(season); 
  }

  let program = season.programs.find(x => x.program === p.name);
  if (!program) {
    program = Program(p);
    season.programs.push(program);
  }

  program.episodes.push(Episode(p));
});

